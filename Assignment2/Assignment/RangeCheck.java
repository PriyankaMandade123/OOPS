public class RangeCheck {
    public static void main(String[] args) {
        int number = 35;  // You can change this value to test with different numbers

        // Check if number is between 20 and 50 using logical AND (&&)
        System.out.println((number >= 20 && number <= 50) ? "Number is within the range of 20 to 50" : "Number is outside the range of 20 to 50");
    }
}
